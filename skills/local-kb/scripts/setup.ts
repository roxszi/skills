/**
 * local-kb 首次建库脚本。
 *
 * 用法：
 *   bun run scripts/setup.ts <kb-path> --schema <schema.yaml>
 *   bun run scripts/setup.ts <kb-path> --mock              # 使用内置 mock schema
 *
 * 流程：
 *   1. 解析 schema.yaml（极简 YAML 解析器，零依赖）
 *   2. 生成 .schema.sql（含 FTS5 + 触发器 + 索引）
 *   3. 跑 ensureSchema 幂等建表
 *   4. 写 slug_rule.json（供 ingest.ts 用）
 *   5. 写 README 模板
 *   6. 写 .schema-version 文件
 *
 * 反模式（直接报错）：
 *   - schema.yaml 缺必填字段 → 报错
 *   - 库目录已存在且非空 → 报错（避免覆盖）
 *   - .schema.sql 已存在 → 报错（提示用户用 migrate）
 */

import { mkdirSync, writeFileSync, existsSync, readFileSync, readdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { resolveKbPath, ensureSchema, describeDb } from "./db.ts";
import { parseYaml, type YamlObject } from "./yaml.ts";

const __dirname = dirname(fileURLToPath(import.meta.url));

// ===== Schema 解析 =====

interface FieldDef {
  name: string;
  type: string;
  unique?: boolean;
  indexed?: boolean;
  fts?: boolean;
  json?: boolean;
  related?: boolean;  // 该字段参与 related.ts 的默认关联发现
}

interface SchemaYaml {
  collection: {
    name: string;
    schema_version: number | string;
    primary_table: string;
    primary_key?: string;
  };
  fields: {
    required: FieldDef[];
    optional?: FieldDef[];
  };
  slug_rule?: {
    parts: Array<{ field: string; transform?: string }>;
    separator?: string;
    unique_fields?: string[];
  };
}

function loadSchemaYaml(path: string): SchemaYaml {
  if (!existsSync(path)) {
    throw new Error(`schema.yaml not found: ${path}`);
  }
  const obj = parseYaml(readFileSync(path, "utf-8"));

  // 必填字段校验
  if (!obj.collection) throw new Error("schema.yaml 缺少 collection 节");
  const c = obj.collection as YamlObject;
  if (!c.name) throw new Error("schema.yaml 缺少 collection.name");
  if (!c.schema_version) throw new Error("schema.yaml 缺少 collection.schema_version");
  if (!c.primary_table) throw new Error("schema.yaml 缺少 collection.primary_table");

  if (!obj.fields) throw new Error("schema.yaml 缺少 fields 节");
  const f = obj.fields as YamlObject;
  if (!f.required) throw new Error("schema.yaml 缺少 fields.required");

  return obj as unknown as SchemaYaml;
}

// ===== SQL 生成 =====

const SQL_TYPE_MAP: Record<string, string> = {
  string: "TEXT",
  int: "INTEGER",
  integer: "INTEGER",
  number: "REAL",
  text: "TEXT",
  iso8601: "TEXT",
  date: "TEXT",
  path: "TEXT",
  boolean: "INTEGER",
  "string[]": "TEXT NOT NULL DEFAULT '[]'",
};

function generateSchemaSql(schema: SchemaYaml): string {
  const { primary_table, schema_version } = schema.collection;
  const primaryKey = schema.collection.primary_key ?? "slug";

  const allFields: FieldDef[] = [
    ...schema.fields.required,
    ...(schema.fields.optional ?? []),
  ];

  // 主表
  const colDefs: string[] = [];
  for (const f of allFields) {
    if (f.name === primaryKey) continue; // 主键单独定义
    const sqlType = SQL_TYPE_MAP[f.type] ?? "TEXT";
    const notNull = schema.fields.required.some((r) => r.name === f.name) ? " NOT NULL" : "";
    const unique = f.unique ? " UNIQUE" : "";
    colDefs.push(`  ${f.name} ${sqlType}${notNull}${unique}`);
  }
  const pkType = allFields.find((f) => f.name === primaryKey)?.type === "int" ? "INTEGER" : "TEXT";

  let sql = `-- schema_version: ${schema_version}\n`;
  sql += `-- primary_table: ${primary_table}\n`;
  sql += `-- primary_key: ${primaryKey}\n`;
  sql += `-- generated by local-kb setup.ts at ${new Date().toISOString()}\n`;
  sql += `-- DO NOT EDIT THIS FILE MANUALLY — use migrate to update schema\n\n`;

  sql += `CREATE TABLE IF NOT EXISTS ${primary_table} (\n`;
  sql += `  ${primaryKey} ${pkType} PRIMARY KEY,\n`;
  sql += colDefs.join(",\n");
  sql += `\n);\n\n`;

  // FTS5 虚拟表
  const ftsFields = allFields.filter((f) => f.fts);
  if (ftsFields.length > 0) {
    const ftsCols = ftsFields.map((f) => f.name).join(", ");
    sql += `CREATE VIRTUAL TABLE IF NOT EXISTS ${primary_table}_fts USING fts5(\n`;
    sql += `  ${ftsCols},\n`;
    sql += `  content='${primary_table}', content_rowid='rowid',\n`;
    sql += `  tokenize='unicode61 remove_diacritics 2'\n`;
    sql += `);\n\n`;

    // 触发器
    const ftsInsert = ftsFields.map((f) => `COALESCE(new.${f.name}, '')`).join(", ");
    const ftsDelete = ftsFields.map((f) => `COALESCE(old.${f.name}, '')`).join(", ");

    sql += `CREATE TRIGGER IF NOT EXISTS ${primary_table}_ai AFTER INSERT ON ${primary_table} BEGIN\n`;
    sql += `  INSERT INTO ${primary_table}_fts(rowid, ${ftsCols}) VALUES (new.rowid, ${ftsInsert});\n`;
    sql += `END;\n\n`;

    sql += `CREATE TRIGGER IF NOT EXISTS ${primary_table}_ad AFTER DELETE ON ${primary_table} BEGIN\n`;
    sql += `  INSERT INTO ${primary_table}_fts(${primary_table}_fts, rowid, ${ftsCols}) VALUES('delete', old.rowid, ${ftsDelete});\n`;
    sql += `END;\n\n`;

    sql += `CREATE TRIGGER IF NOT EXISTS ${primary_table}_au AFTER UPDATE ON ${primary_table} BEGIN\n`;
    sql += `  INSERT INTO ${primary_table}_fts(${primary_table}_fts, rowid, ${ftsCols}) VALUES('delete', old.rowid, ${ftsDelete});\n`;
    sql += `  INSERT INTO ${primary_table}_fts(rowid, ${ftsCols}) VALUES (new.rowid, ${ftsInsert});\n`;
    sql += `END;\n\n`;
  }

  // 索引
  const indexedFields = allFields.filter((f) => f.indexed || f.unique);
  for (const f of indexedFields) {
    if (f.name === primaryKey) continue;
    sql += `CREATE INDEX IF NOT EXISTS idx_${primary_table}_${f.name} ON ${primary_table}(${f.name});\n`;
  }
  sql += "\n";

  // schema_meta
  sql += `CREATE TABLE IF NOT EXISTS schema_meta (\n`;
  sql += `  key   TEXT PRIMARY KEY,\n`;
  sql += `  value TEXT NOT NULL\n`;
  sql += `);\n`;

  return sql;
}

// ===== Slug rule 生成 =====

function generateSlugRuleJson(schema: SchemaYaml): string {
  // 收集 related: true 字段，写入 related_fields 数组（供 related.ts 默认值用）
  const allFields: FieldDef[] = [
    ...schema.fields.required,
    ...(schema.fields.optional ?? []),
  ];
  const relatedFields = allFields
    .filter((f) => f.related)
    .map((f) => f.name);
  const rule: Record<string, unknown> = {
    ...(schema.slug_rule ?? { parts: [], separator: "_" }),
    related_fields: relatedFields,
  };
  return JSON.stringify(rule, null, 2);
}

