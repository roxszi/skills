// ===== Mock schema（自测用） =====

function mockSchemaYaml(): string {
  return `collection:
  name: "test-kb"
  schema_version: 1
  primary_table: items
  primary_key: slug

fields:
  required:
    - { name: title,         type: string }
    - { name: first_author,  type: string, related: true }
    - { name: year,          type: int }
    - { name: fetched_at,    type: iso8601 }
  optional:
    - { name: doi,           type: string, unique: true }
    - { name: tags,          type: "string[]", json: true, related: true }
    - { name: abstract,      type: text, fts: true }
    - { name: fulltext,      type: text, fts: true }
    - { name: journal,       type: string, related: true }

slug_rule:
  parts:
    - { field: first_author, transform: "lower+strip_nonascii" }
    - { field: year }
    - { field: journal,      transform: "lower+strip_nonascii+slice(12)" }
    - { field: title,        transform: "lower+strip_nonascii+split_space+slice_words(4)+join_underscore" }
  separator: "_"
  unique_fields: [doi]
`;
}

// ===== CLI =====

function parseArgs(): {
  kbPath: string | null;
  schemaPath: string | null;
  mock: boolean;
} {
  let kbPath: string | null = null;
  let schemaPath: string | null = null;
  let mock = false;
  for (let i = 2; i < process.argv.length; i++) {
    const a = process.argv[i];
    const next = process.argv[i + 1];
    if (a === "--schema" && next) {
      schemaPath = resolve(next);
      i++;
    } else if (a === "--mock") {
      mock = true;
    } else if (a === "--help" || a === "-h") {
      console.log(`用法: setup.ts <kb-path> [options]
  <kb-path>              库路径（如 ./my-kb 或 C:/Data/MyKB）
  --schema <file>        schema.yaml 路径
  --mock                 使用内置 mock schema（自测用）
  --help, -h             本帮助
`);
      process.exit(0);
    } else if (!kbPath) {
      kbPath = resolve(a);
    }
  }
  return { kbPath, schemaPath, mock };
}

const args = parseArgs();
if (!args.kbPath && !args.mock) {
  console.error("需要 <kb-path> 或 --mock，或 --help");
  process.exit(1);
}

const kbPath = args.kbPath ?? "./test-kb";
const { rootDir } = resolveKbPath(kbPath);

// 1. 检查目录状态
if (existsSync(rootDir)) {
  const entries = readdirSync(rootDir);
  if (entries.length > 0) {
    console.error(`>>> 错误：${rootDir} 目录已存在且非空（${entries.length} 项）`);
    console.error(`>>> 如需重建，请先删除该目录或用 migrate.ts 升级 schema`);
    process.exit(1);
  }
} else {
  mkdirSync(rootDir, { recursive: true });
  console.log(`>>> 创建目录：${rootDir}`);
}

// 2. 加载 schema
let schema: SchemaYaml;
if (args.mock) {
  const tmpPath = `${rootDir}/_mock.schema.yaml`;
  writeFileSync(tmpPath, mockSchemaYaml(), "utf-8");
  schema = loadSchemaYaml(tmpPath);
  console.log(`>>> mock schema 写入：${tmpPath}`);
} else {
  schema = loadSchemaYaml(args.schemaPath!);
}

// 3. 生成 .schema.sql
const schemaSql = generateSchemaSql(schema);
const schemaSqlPath = `${rootDir}/.schema.sql`;
writeFileSync(schemaSqlPath, schemaSql, "utf-8");
console.log(`>>> schema SQL 写入：${schemaSqlPath}`);

// 4. 跑 ensureSchema（幂等建表）
const result = ensureSchema(kbPath);
console.log(`>>> schema ${result.created ? "created" : "verified"}, version = v${result.version}`);

// 5. 写 slug_rule.json
const slugRulePath = `${rootDir}/.slug-rule.json`;
writeFileSync(slugRulePath, generateSlugRuleJson(schema), "utf-8");
console.log(`>>> slug rule 写入：${slugRulePath}`);

// 6. 写 README
const readmePath = `${rootDir}/README.md`;
writeFileSync(readmePath, generateReadme(schema), "utf-8");
console.log(`>>> README 写入：${readmePath}`);

// 7. 保存原始 schema.yaml（mock 也保存成 schema.yaml，保持与 ingest 期望一致）
const savedPath = `${rootDir}/schema.yaml`;
if (!existsSync(savedPath)) {
  if (args.mock) {
    writeFileSync(savedPath, mockSchemaYaml(), "utf-8");
    console.log(`>>> schema.yaml（mock）写入：${savedPath}`);
  } else if (args.schemaPath) {
    writeFileSync(savedPath, readFileSync(args.schemaPath, "utf-8"), "utf-8");
    console.log(`>>> schema.yaml 备份：${savedPath}`);
  }
}

console.log();
console.log(">>> 当前数据库状态：");
describeDb(kbPath);
console.log();
console.log(`>>> 建库完成：${kbPath}`);
console.log(`>>> 下一步：bun run scripts/ingest.ts ${kbPath} --mock`);